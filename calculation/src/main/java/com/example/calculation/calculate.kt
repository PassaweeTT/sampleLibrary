package com.example.calculation

class calculate {
    companion object {
        fun add(a: Int, b: Int): Int {
            return a + b
        }

        fun sub(a: Int, b: Int): Int {
            return a - b
        }

        fun mul(a: Int, b: Int): Int {
            return a * b
        }

        fun div(a: Int, b: Int): Int {
            return a / b
        }
    }
}